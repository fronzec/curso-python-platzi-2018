window = turtle.Screen()

# Creamos una instancia de tortuga
tortuga = turtle.Turtle()

# Le decimos a la tortuga que se mueva 100px hacia adelante
tortuga.forward(100)
tortuga.right(90)

tortuga.forward(100)
tortuga.right(90)

tortuga.forward(100)
tortuga.right(90)

tortuga.forward(100)
tortuga.right(90)

# Le decimos a la ventana que no se cierre
window.mainloop()
